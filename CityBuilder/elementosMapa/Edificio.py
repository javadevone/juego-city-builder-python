class Edificio:
    def __init__(self, nombre, categoria, precioOro, precioPiedra, precioMadera, color, referenciaLienzo):
        self.nombre = nombre 
        self.categoria = categoria 
        self.precioOro = precioOro 
        self.precioPiedra = precioPiedra
        self.precioMadera = precioMadera
        self.color = color
        self.referenciaLienzo = referenciaLienzo